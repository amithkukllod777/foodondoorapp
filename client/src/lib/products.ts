// ─── DB Product Adapter ────────────────────────────────────────────────────────
// Converts a DB product row (from tRPC trpc.products.*) to the frontend Product type.
// DB stores: price/mrp in rupees (int), rating as rating*10 (e.g. 45 = 4.5), reviewCount
// Frontend expects: originalPrice, reviews, rating as float
export interface DbProduct {
  id: number;
  name: string;
  handle: string;
  category: string;
  price: number;
  mrp: number;
  discount: number;
  weight: string | null;
  description: string | null;
  image: string;
  images: unknown; // JSON array from DB
  isBestseller: boolean;
  isTrending: boolean;
  isNew: boolean;
  available: boolean;
  rating: number; // stored as rating*10
  reviewCount: number;
  sortOrder: number;
  // Metafields
  dietaryPreferences?: unknown; // JSON array from DB
  allergenInfo?: string | null;
  nutType?: string | null;
  processingMethod?: string | null;
  foodProductForm?: string | null;
  createdAt?: Date;
  updatedAt?: Date;
}

export function dbProductToFrontend(p: DbProduct): Product {
  const imgs = Array.isArray(p.images) ? (p.images as string[]) : [];
  const dietaryPrefs = Array.isArray(p.dietaryPreferences)
    ? (p.dietaryPreferences as string[])
    : typeof p.dietaryPreferences === "string"
    ? JSON.parse(p.dietaryPreferences as string)
    : undefined;
  return {
    id: p.id,
    name: p.name,
    handle: p.handle,
    category: p.category,
    price: p.price,
    originalPrice: p.mrp,
    discount: p.discount,
    weight: p.weight || "",
    description: p.description || "",
    image: p.image,
    images: imgs.length > 0 ? imgs : [p.image],
    isBestseller: p.isBestseller,
    isTrending: p.isTrending,
    isNew: p.isNew,
    available: p.available,
    rating: p.rating / 10, // convert back to float e.g. 45 → 4.5
    reviews: p.reviewCount,
    // Metafields
    dietaryPreferences: dietaryPrefs,
    allergenInfo: p.allergenInfo || undefined,
    nutType: p.nutType || undefined,
    processingMethod: p.processingMethod || undefined,
    foodProductForm: p.foodProductForm || undefined,
  };
}
