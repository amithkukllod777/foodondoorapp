async function main() {
  const conn = await mysql2.createConnection(process.env.DATABASE_URL);
  console.log('Connected to DB');

  // Check existing count
  const [rows] = await conn.execute('SELECT COUNT(*) as count FROM products');
  const existing = rows[0].count;
  console.log(`Existing products in DB: ${existing}`);

  let inserted = 0;
  let skipped = 0;
  let failed = 0;

  for (const p of products) {
    try {
      const s3Image = mapImg(p.image);
      const s3Images = mapImgs(p.images);

      await conn.execute(
        `INSERT INTO products (id, name, handle, category, price, mrp, discount, weight, description, image, images, isBestseller, isTrending, isNew, available, rating, reviewCount, sortOrder)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
         ON DUPLICATE KEY UPDATE
           name=VALUES(name), image=VALUES(image), images=VALUES(images), price=VALUES(price), mrp=VALUES(mrp),
           discount=VALUES(discount), weight=VALUES(weight), description=VALUES(description),
           isBestseller=VALUES(isBestseller), isTrending=VALUES(isTrending), isNew=VALUES(isNew),
           available=VALUES(available), rating=VALUES(rating), reviewCount=VALUES(reviewCount), sortOrder=VALUES(sortOrder)`,
        [
          p.id, p.name, p.handle, p.category, p.price, p.mrp, p.discount,
          p.weight, p.description, s3Image, JSON.stringify(s3Images),
          p.isBestseller ? 1 : 0, p.isTrending ? 1 : 0, p.isNew ? 1 : 0,
          p.available ? 1 : 0, p.rating, p.reviewCount, p.sortOrder
        ]
      );
      console.log(`✓ [${p.id}] ${p.name.substring(0, 60)}...`);
      inserted++;
    } catch (err) {
      console.error(`✗ [${p.id}] ${p.name.substring(0, 40)}: ${err.message}`);
      failed++;
    }
  }

  await conn.end();
  console.log(`\n✅ Done! ${inserted} upserted, ${skipped} skipped, ${failed} failed`);
}

